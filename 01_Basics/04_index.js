/* Nodemon package

When you build a node js project when run the project it show output.
When again you do some changes in project you again run the project. This was time consuming -> To save this we use nodemon 

nodemon continuesly run your node js project 

To use this:
Install nodemon: npm i nodemon (OR) npm i nodemon -g (it means it is installed in globally. When you use this it is installed locally in whole system or no need to install in separate folder)

After installing if you want to run any file ->use command:  nodemon <file_name>

*/
